export const formatMoney = (number) => {
  return number
    .toLocaleString("en-US", {
      style: "currency",
      currency: "NGN",
    })
    .replace(/^NGN\s*/, "N");
};
export const formatDateWithOrdinal = (dateString) => {
  const date = new Date(dateString);

  const options = {
    year: "numeric",
    month: "long",
    day: "numeric",
  };

  const formattedDate = date.toLocaleString("en-US", options);
  console.log(formattedDate);
  const getOrdinal = (n) => {
    const s = ["th", "st", "nd", "rd"],
      v = n % 100;
    return s[(v - 20) % 10] || s[v] || s[0];
  };

  const day = date.getDate();
  const ordinal = getOrdinal(day);

  return formattedDate.replace(/\d+/, day + ordinal);
};

export const calculateDaysLeft = (dueDate) => {
  const dueDateObj = new Date(dueDate);

  const currentDate = new Date();

  const difference = dueDateObj.getTime() - currentDate.getTime();

  const daysLeft = Math.ceil(difference / (1000 * 60 * 60 * 24));

  const formattedOutput = `${daysLeft} day${daysLeft !== 1 ? "s" : ""} left`;

  return formattedOutput;
};
export const daysSince = (dateString) => {
  const date = new Date(dateString);

  const currentDate = new Date();

  const differenceInMilliseconds = currentDate - date;

  if (differenceInMilliseconds < 0) {
    return "0 day";
  }

  const differenceInDays = Math.floor(
    differenceInMilliseconds / (1000 * 60 * 60 * 24)
  );

  return differenceInDays === 1 ? "1 Day" : `${differenceInDays} Days`;
};
